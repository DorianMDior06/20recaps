let numero = 42;             // Número
let texto = "Hola, mundo";   // String
let esVerdadero = true;      // Booleano
let valorNulo = null;        // Null

// Verificación de tipos
alert(`El tipo de 'numero' es: ${typeof numero}`); // number
alert(`El tipo de 'texto' es: ${typeof texto}`);   // string
alert(`El tipo de 'esVerdadero' es: ${typeof esVerdadero}`); // boolean
alert(`El tipo de 'valorNulo' es: ${typeof valorNulo}`); // object (esto es un comportamiento especial en JavaScript)
